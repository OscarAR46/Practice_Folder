import random

pokemon_dict = {
    "Charmander": Charmander,
    "Squirtle": Squirtle,
    "Bulbasaur": Bulbasaur
}


pokemon_trainer_name = input("Here come Team Rocket, which Pokémon will you use to beat them? Charmander, Squirtle, or Bulbasaur? ").strip().capitalize()
pokemon_trainer_art = pokemon_dict.get(pokemon_trainer_name)

if pokemon_trainer_art is None:
    print("Invalid choice! Go Again!")
    exit()


team_rocket_art = random.choice(list(pokemon_dict.values()))

print(pokemon_trainer_art, "\nI choose you!")
print("Team Rocket chose:", team_rocket_art)


if pokemon_trainer_art == team_rocket_art:
    print("Your Pokémon knocked each other out! I wonder who will win next time?")
elif (
    (pokemon_trainer_art == Charmander and team_rocket_art == Bulbasaur) or
    (pokemon_trainer_art == Squirtle and team_rocket_art == Charmander) or
    (pokemon_trainer_art == Bulbasaur and team_rocket_art == Squirtle)
):
    print("Let's go!", pokemon_trainer_name, "just obliterated", [name for name, art in pokemon_dict.items() if art == team_rocket_art][0] + " They're crying like little babies!")
else:
    print("Oh no, looks like Team Rocket got the best of you this time... ")